    public void ortalamaal(){
        float toplam=0;
        float genelOrt;
        for (Ders ders : ogrenci.Program) {
            toplam+=(float)ders.kredi *(float)ders.not;
        }toplam=(Float)toplam/(float)ogrenci.KullanilanKredi;
        ogrenci.genelOrt=toplam; 
    }
    public void Listele() {
        for (Ders ders : ogrenci.Program) {
            dtm.addRow(new String[]{ders.Ad, Integer.toString(ders.kredi), ders.Gun, ders.Saat, Float.toString(ders.not)});
        }

    }

