    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int column = 0;
        int row = jTable1.getSelectedRow();
        String DersAdi=jTable1.getModel().getValueAt(row, column).toString();
        for (int i = 0; i < ogrenci.Program.size(); i++) {
            if(ogrenci.Program.get(i).Ad.equals(DersAdi)){
                ogrenci.KullanilanKredi-=ogrenci.Program.get(i).kredi;
                ogrenci.Program.remove(i);
                JOptionPane.showMessageDialog(
                    null, "Kaldirilmistir", "Error", JOptionPane.OK_OPTION);
                break;
            }
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

