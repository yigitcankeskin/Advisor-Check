    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int row = jTable1.rowAtPoint(evt.getPoint());
        String num = (String) jTable1.getValueAt(row, 1);
        
        for (int i = 0; i < ogretmen.Ogrenciler.size(); i++) {
            if(ogretmen.Ogrenciler.get(i).Numara.equals(num)){
                ogrenci=ogretmen.Ogrenciler.get(i);
                OgrenciBilgi OB = new OgrenciBilgi(danisman,ogrenci,ogretmen);
                OB.setVisible(true);
                OB.setLocation(250, 250);
                break;
            }
            
        }
        
    }//GEN-LAST:event_jTable1MouseClicked

