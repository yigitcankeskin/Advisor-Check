    public void Listele() {
        String AS;
        for (Ogrenci ogrenci : ogretmen.Ogrenciler) {
            AS = ogrenci.Ad+" "+ogrenci.Soyad;
            dtm.addRow(new String[]{ AS, ogrenci.Numara,Integer.toString(ogrenci.KrediHakki), Integer.toString(ogrenci.KullanilanKredi)});
        }
        
    }

